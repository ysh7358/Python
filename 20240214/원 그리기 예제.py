#빨간색 원 그리기(내부 채우기 fill)

import turtle
t = turtle.Turtle()
t.shape("turtle")
t.color("red")
t.begin_fill()
t.circle(50)
t.end_fill()
t.up()
t.goto(100,0)
t.down()
t.color("yellow")
t.begin_fill()
t.circle(50)
t.end_fill()
t.up()
t.goto(200,0)
t.down()
t.color("green")
t.begin_fill()
t.circle(50)
t.end_fill()
