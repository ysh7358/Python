firstNumber = float(input("첫 번째 수를 입력해 주세요 : "))
secondNumber = float(input("두 번째 수를 입력해 주세요 : "))

if(firstNumber > secondNumber) :
    print("둘 중 큰 수는 " + str(firstNumber) + "입니다")
elif(firstNumber < secondNumber) :
    print("둘 중 큰 수는 " + str(secondNumber) + "입니다")
else :
    print("두 수가 " + str(firstNumber) +  "로 같습니다")

